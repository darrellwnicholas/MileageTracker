#pragma mark - Table view data source



- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    // Return the number of sections.
    return 2;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {

    // Return the number of rows in the section.
    if (section == 0) {
        return 1;
    } else {
    
        return self.cars.count - 1;
    }
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"carCell" forIndexPath:indexPath];
    
    // Configure the cell...
    Car *c = [self.cars objectAtIndex:indexPath.row];
    NSString *miles = [NSString stringWithFormat:@"%li", (long)c.currentMileage];
    cell.textLabel.text = c.name;
    cell.detailTextLabel.text = miles;
    return cell;
}


